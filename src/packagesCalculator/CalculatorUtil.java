package packagesCalculator;

public class CalculatorUtil {
	
	public void add(int a, int b)
	{
		int result=a+b;
		System.out.println("Sum of two numbers are: " + result);
	}
	
	public void subtract(int a, int b)
	{
		int result=a-b;
		System.out.println("Difference of two numbers are: " + result);
	}
	
	public void multiply(int a, int b)
	{
		int result=a*b;
		System.out.println("Product of two numbers are: " + result);
	}
	
	public void divide(int a, int b)
	{
		int result=a/b;
		System.out.println("Division of two numbers are: " + result);
	}

}
